//! The constant-memory invariant, as a test.
//!
//! > **Peak resident memory while analyzing a recording is `O(sites + live set)`
//! > and independent of the number of events.**
//!
//! This is the property that was violated when the analyzers read the whole event
//! stream into a `Vec`: a 1.6 GB recording cost ~4.5 GB in `diff` and ~25 GB in
//! `perfetto`, and a 1.9 GB one was OOM-killed. Memory scaled with total events,
//! while the thing you actually want to inspect — the live set — is orders of
//! magnitude smaller.
//!
//! Each measurement runs in a **fresh subprocess**, because `ru_maxrss` is a
//! high-water mark that never comes back down within a process. The subprocess is
//! this same test binary re-executed into `measure_child` (an `#[ignore]`d test,
//! so it only ever runs when asked for by name). Re-executing ourselves rather
//! than a separate example binary means the child can never be a stale build.

use std::process::Command;

use memscope_proto::{recfmt, EventKind, RawEvent, SiteId};
use memscope_replay::{read_recording_raw, site_stats, stream_events, Timeline};

/// Event counts to compare. The live set is identical in both, so any RSS growth
/// between them is event-count scaling — exactly what must not happen.
const SMALL_EVENTS: usize = 200_000;
const LARGE_EVENTS: usize = 4_000_000; // 20x the events
const LIVE_ALLOCS: usize = 2_000;

/// How much peak RSS may grow across a 20x increase in events. Generous enough to
/// absorb allocator slack and harness noise, far tighter than the linear growth
/// this guards against (4M retained `RecEvent`s alone would be ~160 MB).
const MEM_SLACK_BYTES: u64 = 64 * 1024 * 1024;

// --- child mode --------------------------------------------------------------

const ENV_FILE: &str = "MEMSCOPE_CM_FILE";
const ENV_EVENTS: &str = "MEMSCOPE_CM_EVENTS";

/// The measured subprocess: fold over an already-written recording and report
/// peak RSS. It does **only** the analysis — the recording is generated by the
/// parent, so the generator's buffers can't show up in the measurement.
///
/// `#[ignore]` keeps it out of normal runs; the parent invokes it by exact name.
#[test]
#[ignore]
fn measure_child() {
    let file = std::env::var(ENV_FILE).expect("child needs a recording path");
    let summary = analyze_all(&file);
    // The parent parses these off stdout.
    println!("{summary} peak_rss_bytes={}", peak_rss_bytes());
}

/// Write a churn-shaped recording: `live` allocations that stay alive, and the
/// rest alloc/free pairs that don't. The live set is held constant across sizes,
/// so the only thing varying between runs is the event count.
fn write_recording(file: &str, events: usize, live: usize) {
    use std::io::Write as _;
    let mut b = Vec::with_capacity(1 << 16);
    recfmt::encode_header(&mut b, 4242, "/nonexistent/constant-memory-test", 0);
    recfmt::encode_mark(&mut b, 0, "start");
    recfmt::encode_mark(&mut b, 1, "end");
    recfmt::encode_events_header(&mut b, events as u32);

    let out = std::fs::File::create(file).expect("create synthetic recording");
    let mut out = std::io::BufWriter::with_capacity(1 << 20, out);
    // Flush the encode buffer to disk periodically so generating a huge recording
    // doesn't itself need a huge allocation.
    let mut push = |b: &mut Vec<u8>, kind, addr: u64, size: u64, ts: u64, site: u32| {
        recfmt::encode_event(
            b,
            &RawEvent {
                kind,
                addr,
                size,
                ts_nanos: ts,
                site: SiteId(site),
                thread: 1,
                align: 8,
                seq: 0,
            },
        );
        if b.len() >= (1 << 16) {
            out.write_all(b).expect("write events");
            b.clear();
        }
    };

    let mut written = 0usize;
    push(&mut b, EventKind::Mark, 0, 0, 0, 0);
    written += 1;
    // The permanently-live set: distinct addresses, never freed.
    for i in 0..live.min(events.saturating_sub(2)) {
        push(&mut b, EventKind::Alloc, 0x1000_0000 + i as u64 * 64, 64, i as u64, 1);
        written += 1;
    }
    // Churn: alloc/free pairs reusing a small address window. This is what makes
    // the event count large while the live set stays flat.
    let mut i = 0u64;
    while written + 2 < events {
        let addr = 0x2000_0000 + (i % 4096) * 64;
        push(&mut b, EventKind::Alloc, addr, 16 + (i % 64), i, 2);
        push(&mut b, EventKind::Dealloc, addr, 16 + (i % 64), i + 1, 0);
        written += 2;
        i += 1;
    }
    while written + 1 < events {
        push(&mut b, EventKind::Alloc, 0x3000_0000 + written as u64, 8, written as u64, 3);
        written += 1;
    }
    push(&mut b, EventKind::Mark, 1, 0, i + 2, 1);
    written += 1;
    assert_eq!(written, events, "event block header must match what we wrote");
    out.write_all(&b).expect("write tail");
    out.flush().expect("flush recording");
}

/// Run every fold the analyzers rely on, so this covers the real paths rather
/// than a bare iterator.
fn analyze_all(file: &str) -> String {
    let rec = read_recording_raw(file).expect("read recording");

    let stats = site_stats(stream_events(file).expect("stream"), &rec);
    let alloc_bytes: u64 = stats.iter().map(|s| s.alloc_bytes).sum();

    let tl = Timeline::open(file, &rec).expect("timeline");
    let a = tl.find("start").map(|m| m.index + 1).unwrap_or(0);
    let b = tl.find("end").map(|m| m.index + 1).unwrap_or(tl.event_count());
    let w = tl.window(a, b).expect("window");

    let mut marks = 0;
    tl.for_each_mark(|_, _| marks += 1).expect("marks");
    let end = tl.state_at_end().expect("end state");

    format!(
        "events={} alloc_bytes={} sites={} window_sites={} marks={} live={}",
        tl.event_count(),
        alloc_bytes,
        stats.len(),
        w.b_by_site.len(),
        marks,
        end.live.len()
    )
}

/// Peak resident set size of this process, in bytes.
fn peak_rss_bytes() -> u64 {
    let mut usage: libc::rusage = unsafe { std::mem::zeroed() };
    if unsafe { libc::getrusage(libc::RUSAGE_SELF, &mut usage) } != 0 {
        return 0;
    }
    let max = usage.ru_maxrss as u64;
    // macOS reports bytes; Linux reports kilobytes.
    if cfg!(target_os = "macos") {
        max
    } else {
        max * 1024
    }
}

// --- parent mode -------------------------------------------------------------

/// Re-execute this test binary into [`measure_child`] and return its peak RSS.
///
/// `tag` names the calling test so concurrently running tests use distinct
/// recording files.
fn peak_rss_for(tag: &str, events: usize) -> u64 {
    let exe = std::env::current_exe().expect("test exe path");
    let file = std::env::temp_dir().join(format!("memscope-constmem-{tag}-{events}.mscope"));

    write_recording(file.to_str().expect("utf-8 temp path"), events, LIVE_ALLOCS);

    let out = Command::new(&exe)
        .args(["--exact", "measure_child", "--ignored", "--nocapture", "--test-threads", "1"])
        .env(ENV_FILE, &file)
        .env(ENV_EVENTS, events.to_string())
        .output()
        .expect("run measurement child");
    let _ = std::fs::remove_file(&file);

    let stdout = String::from_utf8_lossy(&out.stdout).into_owned();
    assert!(
        out.status.success(),
        "measurement child failed:\n{stdout}\n{}",
        String::from_utf8_lossy(&out.stderr)
    );

    // Sanity: the child must actually have folded over every event, or we'd be
    // measuring the memory of doing nothing.
    let seen: usize = field(&stdout, "events=").parse().expect("events count");
    assert_eq!(seen, events, "child saw the wrong number of events:\n{stdout}");
    let live: usize = field(&stdout, "live=").parse().expect("live count");
    assert!(live >= LIVE_ALLOCS, "expected the live set to survive, got {live}:\n{stdout}");

    field(&stdout, "peak_rss_bytes=").parse().expect("peak rss")
}

fn field<'a>(out: &'a str, key: &str) -> &'a str {
    out.split_whitespace()
        .find_map(|tok| tok.strip_prefix(key))
        .unwrap_or_else(|| panic!("no {key} in output:\n{out}"))
}

/// The invariant: 20x the events must not mean meaningfully more memory.
#[test]
fn analysis_memory_is_independent_of_event_count() {
    let small = peak_rss_for("growth", SMALL_EVENTS);
    let large = peak_rss_for("growth", LARGE_EVENTS);
    let growth = large.saturating_sub(small);

    eprintln!(
        "peak RSS: {SMALL_EVENTS} events -> {:.1} MB, {LARGE_EVENTS} events -> {:.1} MB (growth {:.1} MB)",
        small as f64 / 1e6,
        large as f64 / 1e6,
        growth as f64 / 1e6,
    );

    assert!(
        growth < MEM_SLACK_BYTES,
        "analysis memory scales with event count: {SMALL_EVENTS} events used {small} B, \
         {LARGE_EVENTS} events used {large} B (growth {growth} B > slack {MEM_SLACK_BYTES} B). \
         Something is retaining the event stream instead of folding over it."
    );
}

/// A recording far larger than any per-event RAM budget must still analyze in a
/// small, *absolute* amount of memory — not merely "no worse than the small case".
#[test]
fn analysis_of_a_large_recording_stays_small() {
    let peak = peak_rss_for("ceiling", LARGE_EVENTS);
    // 4M events is ~136 MB on disk; retaining them as `RecEvent` would be ~160 MB
    // before hashing overhead. A fold over a 2k live set needs a tiny fraction.
    const CEILING: u64 = 96 * 1024 * 1024;
    assert!(
        peak < CEILING,
        "analyzing {LARGE_EVENTS} events peaked at {peak} B, above the {CEILING} B ceiling"
    );
}
