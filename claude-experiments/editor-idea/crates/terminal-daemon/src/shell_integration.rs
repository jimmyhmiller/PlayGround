//! Auto-injected shell integration.
//!
//! When the daemon exec's the user's shell, we want every prompt to
//! emit an OSC 7 (`\e]7;file://host/path\e\\`) so the editor side knows
//! when the cwd changes. We don't want the user to have to edit their
//! `.zshrc`.
//!
//! Approach: write a small set of shim rc files under our data dir and
//! point `ZDOTDIR` at it (zsh). Each shim sources the user's real rc
//! from `$HOME` (or their previously-set `ZDOTDIR`, which we stash in
//! `TERMINAL_BEVY_USER_ZDOTDIR` before launch), then appends an OSC 7
//! emitter to `precmd_functions` + `chpwd_functions`.
//!
//! Bash has no equivalent of `ZDOTDIR` for login shells, so for now
//! bash users don't get auto-injection — we just no-op and they'll fall
//! back to no-cwd-events until/unless they edit their profile.

use std::io::Write;
use std::path::{Path, PathBuf};

const ZSHENV: &str = r#"# auto-generated by terminal-bevy; do not edit by hand.
[ -f "${TERMINAL_BEVY_USER_ZDOTDIR:-$HOME}/.zshenv" ] && source "${TERMINAL_BEVY_USER_ZDOTDIR:-$HOME}/.zshenv"
"#;

const ZPROFILE: &str = r#"# auto-generated by terminal-bevy; do not edit by hand.
[ -f "${TERMINAL_BEVY_USER_ZDOTDIR:-$HOME}/.zprofile" ] && source "${TERMINAL_BEVY_USER_ZDOTDIR:-$HOME}/.zprofile"
"#;

const ZLOGIN: &str = r#"# auto-generated by terminal-bevy; do not edit by hand.
[ -f "${TERMINAL_BEVY_USER_ZDOTDIR:-$HOME}/.zlogin" ] && source "${TERMINAL_BEVY_USER_ZDOTDIR:-$HOME}/.zlogin"
"#;

const ZSHRC: &str = r#"# auto-generated by terminal-bevy; do not edit by hand.
# 1) Restore the user's original ZDOTDIR (we stashed it in
#    TERMINAL_BEVY_USER_ZDOTDIR before exec) so $ZDOTDIR-relative
#    lookups inside their rc work as expected.
if [ -n "$TERMINAL_BEVY_USER_ZDOTDIR" ]; then
  ZDOTDIR="$TERMINAL_BEVY_USER_ZDOTDIR"
else
  ZDOTDIR="$HOME"
fi
unset TERMINAL_BEVY_USER_ZDOTDIR

# 2) /etc/zshrc on macOS sets HISTFILE=${ZDOTDIR:-$HOME}/.zsh_history.
#    That line ran while $ZDOTDIR still pointed at our shim dir, so
#    HISTFILE is currently a stray file under the shim. Re-apply the
#    formula now that ZDOTDIR has been restored. The user's own .zshrc
#    is sourced next and may override HISTFILE; that wins.
HISTFILE="${ZDOTDIR:-$HOME}/.zsh_history"

# 3) Source the user's real .zshrc (best-effort; missing file is fine).
[ -f "$ZDOTDIR/.zshrc" ] && source "$ZDOTDIR/.zshrc"

# 4) zsh has already loaded history from whatever HISTFILE pointed at
#    when the interactive-shell init read it (the wrong path). Re-read
#    from the now-correct HISTFILE so the user's prior history shows up
#    in this session immediately. `-I` would also read incrementally;
#    plain `fc -R` is enough to bring the file in.
[ -r "$HISTFILE" ] && fc -R "$HISTFILE"

# 5) OSC 7 emission on every prompt and on every cd. The editor side
#    parses these to derive the working directory of each terminal.
_tb_emit_cwd() {
  printf '\e]7;file://%s%s\e\\' "${HOST:-localhost}" "$PWD"
}
typeset -ga precmd_functions chpwd_functions
precmd_functions+=(_tb_emit_cwd)
chpwd_functions+=(_tb_emit_cwd)
# Initial emit so we surface the starting cwd before the first prompt fires.
_tb_emit_cwd
"#;

/// Where the shim files live. Lazily written on first call.
fn shim_dir() -> Option<PathBuf> {
    let data = crate::data_dir()?;
    Some(data.join("shell-integration").join("zsh"))
}

/// Ensure the shim files exist on disk. Idempotent; rewrites on every
/// call so a daemon launched after a code upgrade picks up updated
/// integration content automatically (the files are tiny).
pub fn ensure_installed() -> std::io::Result<PathBuf> {
    let dir = shim_dir()
        .ok_or_else(|| std::io::Error::other("no data_dir (HOME unset?)"))?;
    std::fs::create_dir_all(&dir)?;
    write_atomic(&dir.join(".zshenv"), ZSHENV)?;
    write_atomic(&dir.join(".zprofile"), ZPROFILE)?;
    write_atomic(&dir.join(".zshrc"), ZSHRC)?;
    write_atomic(&dir.join(".zlogin"), ZLOGIN)?;
    Ok(dir)
}

fn write_atomic(path: &Path, content: &str) -> std::io::Result<()> {
    let tmp = path.with_extension("tmp");
    {
        let mut f = std::fs::File::create(&tmp)?;
        f.write_all(content.as_bytes())?;
        f.sync_all()?;
    }
    std::fs::rename(&tmp, path)
}

/// Env-var overrides to apply when exec'ing `shell_path`. Returns an
/// empty Vec for shells we don't yet support (bash, fish, …) — the
/// shell still launches, just without cwd reporting.
pub fn env_overrides_for(shell_path: &Path) -> Vec<(String, String)> {
    let name = shell_path
        .file_name()
        .and_then(|n| n.to_str())
        .unwrap_or("");
    if !name.ends_with("zsh") {
        return Vec::new();
    }
    let Ok(dir) = ensure_installed() else {
        return Vec::new();
    };
    let mut out = Vec::with_capacity(2);
    // Stash the user's pre-existing ZDOTDIR (if any) so our .zshrc can
    // restore it before sourcing their rc.
    if let Some(prev) = std::env::var_os("ZDOTDIR") {
        out.push((
            "TERMINAL_BEVY_USER_ZDOTDIR".into(),
            prev.to_string_lossy().into_owned(),
        ));
    }
    out.push(("ZDOTDIR".into(), dir.to_string_lossy().into_owned()));
    out
}
